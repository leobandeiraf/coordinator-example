import UIKit

extension UIColor {
    // MARK: Tint
    static let primary: UIColor = UIColor(named: "primary")!
    static let secondary: UIColor = UIColor(named: "secondary")!
    static let caution: UIColor = UIColor(named: "caution")!
    
    // MARK: Background
    static let divider: UIColor = UIColor(named: "divider")!
    static let border: UIColor = UIColor(named: "border")!
    static let highlighted: UIColor = UIColor(named: "highlighted")!
    static let initialGradient: UIColor = UIColor(named: "initialGradient")!
    static let finalGradient: UIColor = UIColor(named: "finalGradient")!
    
    // MARK: Foreground
    static let highEmphasis: UIColor = UIColor(named: "highEmphasis")!
    static let mediumEmphasis: UIColor = UIColor(named: "mediumEmphasis")!
}
